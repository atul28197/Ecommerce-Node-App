const express = require("express");
const cors = require("cors");
require("dotenv").config();

const app = express();

app.use(cors());
app.use(express.json());

// health route
app.get("/health", (req, res) => {
  res.json({ status: "ok" });
});

const authRoutes = require('./routes/auth.routes');
app.use('/auth', authRoutes);


module.exports = app;
